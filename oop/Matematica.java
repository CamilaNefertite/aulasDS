package poo;

public class Matematica {
    public int somar(int primeiroN, int segundoN) {
        return primeiroN + segundoN;
    }
    
    public int somar(int primeiroN, int segundoN, int terceiroN) {
        return primeiroN + segundoN + terceiroN;
    }
    
    public int somar(int primeiroN, int segundoN, int terceiroN, int quartoN) {
        return primeiroN + segundoN + terceiroN + quartoN;
    }
    
    
    
}
