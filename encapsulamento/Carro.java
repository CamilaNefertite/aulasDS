package atividadeAvaliativa2;
public class Carro {
	
	private String modelo;
	private String fabri;
	private String anoFabri;
																																					
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getModelo() {
		return modelo;
	}
	
	
	public void setFabri(String fabri) {
		this.fabri = fabri;
	}
	public String getFabri() {
		return fabri;
	}
	
	
	public void setAnoFabri(String anoFabri) {
		this.anoFabri = anoFabri ;
	}
	public String getAnoFabri() {
		return anoFabri;
	}										
}
